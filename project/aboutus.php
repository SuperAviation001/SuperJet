DIAL is a joint venture consortium of GMR Group, Airports Authority of India and Fraport. 
In January 2006, the consortium was awarded the concession to operate, manage and develop the IGI Airport following an international competitive bidding process. DIAL signed the Operations, Management and Development Agreement (OMDA) on April 4, 2006 with the AAI. The initial term of the concession is 30 years extendable by a further 30 years.<br/>
Besides upgrading the existing terminals, DIAL has already commissioned a new runway 11-29 at IGI Airport on Sepember 25, 2008.
The terminal has a modern 5 level in-line baggage handling system to eliminate baggage X-ray prior to check in, spacious security hold area with extensive F&B and retail facilities, special contact zone for passengers with special needs and baggage handling area on a separate level allowing greater space for passenger amenities.<br/>
In March 2010, DIAL has completed the construction of integrated passenger terminal (Terminal 3).
In subsequent stages, the airport will be further developed with the increase in passenger demand and more terminals and runways would be added in a modular manner to form a ‘U’ shaped complex with an ultimate design capacity of 100 million passengers per annum.<br/>
